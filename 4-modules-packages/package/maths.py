def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b

def multiplication(a, b):
    return a * b

def division(a, b):
    return a / b
def power(a, b):
    return a ** b
def modulus(a, b):
    return a % b
def floor_division(a, b):
    return a // b
def square_root(a):
    return a ** 0.5
def cube_root(a):
    return a ** (1/3)
